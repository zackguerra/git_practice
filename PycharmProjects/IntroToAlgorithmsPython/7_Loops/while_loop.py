# While-loop
# A while loop is similar to an if statement.
# it repeats some code while the given condition is true.
# it will continue to execute the code for as long the condition is true!

# syntax
# while __contidion__:
#     code to repeat

num = 1
while num <= 10:
    print(num)
    num += 1

print("Done!")

